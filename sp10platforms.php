I have met such a great number of fantastic people in my time at APO, and it was sad to see so many of them graduate and move on.  After this semester, even more awesome people will be leaving our chapter.  I want to keep those bonds between these future alumni and the continuing members strong as well as allow new pledges to meet firsthand those who have contributed so much to the history of Gamma Gamma in a fellowship environment.  
