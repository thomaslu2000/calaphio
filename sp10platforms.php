<p style="margin: 1.5em 0px;">
Dearest Gamma Gamma Chapter,<br /><br />

Throughout my active career in Alpha Phi Omega, I've always felt the need
to give back to the community that made me so much of who I am now. As a
pledge, I was the quietest, most awkward person ever, but I've felt myself
grow and change every semester since. I uncled for Dip n' Dots WK
semester, and I began to realize how bad of a pledge I was from the
perspective of an active. Subsequently, I bigged for Pineapple Express ST
semester, Hot Pockets JM semester, and Pimped Out Penguins this semester.
My approach to bigging has never changed: get the littles involved, and
they will find ways to enjoy themselves throughout the semester.Keeping
my own failings as a pledge in mind, I try to make sure that my littles
avoid the mistakes I made.<br /><br />

At this point, you may be wondering why I'm telling you all this. Well,
to be honest, since a lot of you probably don't know me, this is my
introduction to you. APO has consumed the majority of my college life,
and my small fam constitutes a large part of my identity every semester. 
But more importantly, the attitude I bring to bigging is a perfect
indicator of how I would handle the position of Admin VP.<br /><br />

The office entails three main responsibilities: organizing, editing, and
overseeing the production of the Stylus; maintaining, repairing, and
improving the Chapter Website; and reserving rooms for any events that
require them. Admittedly, there isn't much I can do in terms of improving
the room reservation process. I can only promise to do it in a timely
fashion as to be competitive in the selection process, and to consider
many things that would make everyone's lives easier, such as location,
number of electrical outlets, and room temperature. However, the other
two tasks are indeed areas which can, and to the best of my abilities,
will be improved upon.<br /><br />

The chapter website is a great tool for all of us, and it has been
well-conceived and maintained.  I won't go into detail here, but there are
multiple areas that my officers/committee and I would work with to
streamline and to improve the user interface for a more enjoyable
experience.  Specifically, I will try to make it easier to track one's
progress through your requirements, and to make it easier to plan out
one's semester in terms of these requirements with focused calendars.  I
won't know exactly how these changes are going to be implemented until I
dive into the code, but these are my general goals.<br /><br />

The Stylus is where I would enact the broadest changes, beginning with the
layout.  Newspapers have multiple sections, clearly labeled, so that you
can find the kind of information you want more easily.  While our Stylus
may not be large enough to merit this elaborate structural overhaul,
rearranging and grouping articles together by theme or content and then
labelling them under a section title, which would be listed in a table of
contents, shouldn't be very hard and allows us to find what we're looking
for, especially when skimming through older Styluses for something
specific.<br /><br />

Incentives to write for the Stylus should, in my opinion, follow the
pattern we have for paying dues.  Those who write for earlier Styluses
would get a larger portion of a leadership credit than those who write for
later Styluses.  This will bring more submissions overall, but will also
even out the sizes of early and late semester Styluses.  Hopefully, with
some changes to the website, we can start to track leadership credits,
even in this fractional form.<br /><br />

Finally, I realize that the Stylus is completely black and white to save
time and money, but some submissions simply lose their effect and charm
when stripped of color.  So, a change that I would propose for discussion
is the introduction of 2 colored pages, which would be largely reserved
for pictures, cartoons, optical illusions, and other graphics.<br /><br />

Being a part of ExComm would be a true honor, and would also allow me the
opportunity to make a larger impact in the community that I serve, and
which has served me so profoundly in return.  I've been around for a while
now, and have experienced ideas and changes that work, and others that
haven't worked very well.  Taking a position of relative prominence gives
me the chance to suggest and test some ideas that are not related to my
position, but to ExComm in general.  Lastly, being a big so many times has
allowed me to connect with both pledges and actives, and has given me
insight into the stresses associated with various periods of the semester.
I hope to be an approachable member of ExComm who anybody can work with
to try to relieve these tensions.<br /><br />

Thanks for reading all this!  I don't think I'd ever write a Stylus
article this long...<br /><br />

With Leadership, Friendship, and Service in my heart,<br />
Hobart Lai
