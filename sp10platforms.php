I also intend to create large-scale chapter-wide fellowships at least once a month to ensure that members have the chance to keep contact with all their Brothers and not just their inner circle of friends on a constant basis.  Again, these events will be on the scope of something like GG Picnic or GG Olympics and will be publicized weeks in advance to ensure high attendance and enthusiasm.  It is important for us as a chapter to truly bond with each and every other member if we are to be considered Brothers.  
</p>
<p style="margin: 1.5em 0px;">
I also want to create weekly fellowships that appeal to a large variety of people.  Something that I will bring back are the food fellowships that explore restaurants that are not commonly frequented by Berkeley students.  Tasty Thursdays and Fatty Fridays were past fellowships that did this exact task, yet they have sadly disappeared in recent semesters.  Simple outdoor field days that encourage physical activities or sports games would also be a great opportunity to bond and build a sense of teamwork with your brothers.  
</p>
<h3 style="text-decoration: none; font-weight: bold; font-size: 1.1em;">Interchapter</h3>
<p style="margin: 1.5em 0px;">
The IC events have always been a great chance to get to know Brothers from other chapters across the state.  However, there always seems to be a pattern in which we, Gamma Gamma, tend to flock into our own circle and mingle with each other rather than IC Brothers.  Therefore, IC brothers can sometimes have trouble initiating conversation with us since there are so many of us and it can be intimidating to approach us all.  
</p>
<p style="margin: 1.5em 0px;">
I intend to add maybe one more IC event to our calendar.  Although I have not yet decided the exact nature of the event yet, I will be discussing it further with other members to finalize the logistics.  The event, however, will most definitely be structured in such a way that encourages interaction with brothers from other chapters, possibly involving teams composed of members from more than one chapter.  An idea is a scavenger hunt, similar to Iota Phiís, that would disperse various Gamma Gammas amongst other brothers to foster the interchapter interaction.  Whatever the event eventually ends up being, it will prevent us from socializing only amongst ourselves and will encourage us to talk to IC brothers.  
</p>
<p style="margin: 1.5em 0px;">
It is very important for us to understand how APO is run in other chapters in order to better comprehend the different methods of implementing leadership, friendship, and service across the various chapters of the nation.  In order to accomplish this, we must be willing to take the first step and come outside our safety zone of already close friends.  
</p>
<h3 style="text-decoration: none; font-weight: bold; font-size: 1.1em;">Alumni</h3>
