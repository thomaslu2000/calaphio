</p>
<p style="margin: 1.5em 0px;">
On the fellowship side of things, Iíd like to set up more events that allow us to have fun with the alumni, in addition to stuff like Alumni Bonfire.  Some ideas that I am considering are basketball or football games, cooking, rock climbing, or biking to San Francisco.  These activities appeal to all types of people, including alumni and actives alike, and will let new members learn about the roots of the chapter while older members can see the new directions that our chapter is taking.  
</p>
